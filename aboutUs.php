    <script>
        let slider = document.getElementById('locationSliderTrack');
        let sliderTranslation = 0;

        function toggleSection(direction) {
            if (direction == 'next') {
                if (sliderTranslation < 3) {
                    sliderTranslation = sliderTranslation + 1;
                    slider.style.transform = `translateX(-${sliderTranslation * 100}%)`;
                } else {
                    slider.style.transform = `translateX(-${0 * 100}%)`;
                    sliderTranslation = 0;
                }
            } else if (direction == 'previous') {
                if (sliderTranslation > 0) {
                    sliderTranslation = sliderTranslation - 1;
                    slider.style.transform = `translateX(-${sliderTranslation * 100}%)`;
                } else {
                    slider.style.transform = `translateX(-${3 * 100}%)`;
                    sliderTranslation = 3;
                }
            }
        }
    </script>
